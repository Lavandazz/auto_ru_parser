"""
=== НАСТРОЙКИ ПАРСЕРА АВТОМОБИЛЕЙ ===

Для работы парсера необходимо заполнить следующие параметры:

AUTO (строка) - марка автомобиля
  • Пример: "Mini", "BMW", "Audi"
  • Текущее значение: {auto}

MODEL (список строк) - модели автомобиля
  • Пример: ["Countryman", "Hatch"], ["X5", "X3"], ["A4", "A6"]
  • Можно указать несколько моделей
  • Текущее значение: {model}

YEAR_FROM (целое число) - начальный год выпуска
  • Пример: 2018, 2020, 2015
  • Текущее значение: {year_from}

YEAR_TO (целое число) - конечный год выпуска
  • Пример: 2021, 2023, 2020
  • Текущее значение: {year_to}

VOLUME (строка) - объем двигателя
  • Пример: "2.0", "1.6", "3.0"
  • Текущее значение: {volume}

PRICE (целое число) - максимальная цена в рублях
  • Пример: 2700000, 1500000, 5000000
  • Текущее значение: {price}

ИЗМЕНЕНИЕ ПАРАМЕТРОВ:
1. Найдите блок с настройками выше
2. Измените значения после знака =
3. Запустите парсер

ПРИМЕР ИЗМЕНЕНИЯ:
AUTO = "BMW"
MODEL = ["X5", "X3"]
YEAR_FROM = 2020
YEAR_TO = 2023
VOLUME = "3.0"
PRICE = 4000000
"""


